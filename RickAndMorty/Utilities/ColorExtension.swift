import Foundation
import SwiftUI

extension Color {
    
    static func lightGray() -> Color {
        return Color(red: 236/255, green: 237/255, blue: 241/255)
    }
    
    static func lightBlue() -> Color {
        return Color(red: 234/255, green: 238/255, blue: 252/255)
    }
    
    static func lightGreen() -> Color {
        return Color(red: 227/255, green: 253/255, blue: 228/255)
    }
    
    static func lightRed() -> Color {
        return Color(red: 253/255, green: 227/255, blue: 227/255)
    }
}
